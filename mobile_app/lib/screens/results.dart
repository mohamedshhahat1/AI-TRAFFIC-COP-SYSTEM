import 'package:flutter/material.dart';
import '../services/api_service.dart';

/// Results & Evaluation Screen - Live system metrics
class ResultsScreen extends StatefulWidget {
  const ResultsScreen({super.key});

  @override
  State<ResultsScreen> createState() => _ResultsScreenState();
}

class _ResultsScreenState extends State<ResultsScreen> {
  Map<String, dynamic> _stats = {};
  Map<String, dynamic> _events = {};
  Map<String, dynamic> _health = {};
  bool _isLoading = true;

  @override
  void initState() {
    super.initState();
    _loadData();
  }

  Future<void> _loadData() async {
    setState(() => _isLoading = true);
    final results = await Future.wait([
      ApiService.fetchCameraStats(),
      ApiService.fetchEventMetrics(),
      ApiService.fetchHealth(),
    ]);
    setState(() {
      _stats = results[0];
      _events = results[1];
      _health = results[2];
      _isLoading = false;
    });
  }

  @override
  Widget build(BuildContext context) {
    final inference = (_health['ai_gateway'] as Map?)?['inference'] as Map? ?? {};

    return Scaffold(
      appBar: AppBar(title: const Text('📊 Results & Evaluation')),
      body: RefreshIndicator(
        onRefresh: _loadData,
        child: _isLoading
            ? const Center(child: CircularProgressIndicator())
            : ListView(
                padding: const EdgeInsets.all(16),
                children: [
                  const Text('System Performance Metrics', style: TextStyle(fontSize: 18, fontWeight: FontWeight.bold)),
                  const SizedBox(height: 4),
                  Text('All values from live AI pipeline', style: TextStyle(color: Colors.grey[400], fontSize: 12)),
                  const SizedBox(height: 16),

                  _metricTile('🎯', 'Avg Detection Confidence', '${_stats['avg_confidence'] ?? 0}%', 'YOLOv8 model confidence'),
                  _metricTile('⚡', 'Average FPS', '${_stats['fps'] ?? 0}', 'Frames per second (CPU)'),
                  _metricTile('🎞️', 'Frames Processed', '${_stats['frame'] ?? 0}', 'Total frames analyzed'),
                  _metricTile('🚗', 'Vehicles Detected', '${_totalDetections()}', 'Cumulative detections'),
                  _metricTile('🚨', 'Violations Detected', '${_events['total_handled'] ?? 0}', 'Traffic violations caught'),
                  _metricTile('⏱️', 'Avg Processing Time', '${(inference['avg_latency_ms'] ?? 0).toStringAsFixed(1)} ms', 'Per frame latency'),
                  _metricTile('📡', 'Event Bus Success Rate', '${_successRate()}%', 'Events delivered successfully'),
                  _metricTile('🔥', 'Total Events', '${_events['total_emitted'] ?? 0}', 'Generated by pipeline'),
                  _metricTile('🧠', 'AI Inferences', '${inference['total_inferences'] ?? 0}', 'Model forward passes'),
                  _metricTile('⏰', 'Uptime', _formatUptime(inference['uptime_seconds'] ?? 0), 'Since gateway started'),

                  const SizedBox(height: 24),
                  const Text('Detection Breakdown', style: TextStyle(fontSize: 18, fontWeight: FontWeight.bold)),
                  const SizedBox(height: 12),
                  ..._buildDetectionBreakdown(),

                  const SizedBox(height: 24),
                  const Text('Technology Stack', style: TextStyle(fontSize: 18, fontWeight: FontWeight.bold)),
                  const SizedBox(height: 12),
                  _techTile('👁️', 'Detection', 'YOLOv8 (Ultralytics)'),
                  _techTile('🎯', 'Tracking', 'DeepSORT'),
                  _techTile('🔮', 'Prediction', 'Physics-Based TTC Engine'),
                  _techTile('⚙️', 'Backend', 'FastAPI + SQLAlchemy'),
                  _techTile('📊', 'Frontend', 'React.js'),
                  _techTile('📱', 'Mobile', 'Flutter'),
                  _techTile('🔥', 'Events', 'Custom Event Bus (Pub/Sub)'),
                  _techTile('📈', 'Monitoring', 'Prometheus-Compatible'),
                  _techTile('🐳', 'Deploy', 'Docker Compose'),
                ],
              ),
      ),
    );
  }

  Widget _metricTile(String icon, String label, String value, String note) {
    return Card(
      margin: const EdgeInsets.only(bottom: 8),
      child: ListTile(
        leading: Text(icon, style: const TextStyle(fontSize: 24)),
        title: Text(label, style: const TextStyle(fontSize: 13)),
        subtitle: Text(note, style: TextStyle(fontSize: 11, color: Colors.grey[500])),
        trailing: Text(value, style: const TextStyle(fontSize: 16, fontWeight: FontWeight.bold, color: Colors.blue)),
      ),
    );
  }

  Widget _techTile(String icon, String label, String value) {
    return ListTile(
      dense: true,
      leading: Text(icon, style: const TextStyle(fontSize: 20)),
      title: Text(label, style: TextStyle(fontSize: 12, color: Colors.grey[400])),
      trailing: Text(value, style: const TextStyle(fontSize: 13, fontWeight: FontWeight.w500)),
    );
  }

  List<Widget> _buildDetectionBreakdown() {
    final counts = _stats['detection_counts'] as Map? ?? {};
    if (counts.isEmpty) return [const Text('No detections yet', style: TextStyle(color: Colors.grey))];
    final total = counts.values.fold<num>(0, (a, b) => a + (b as num));
    return counts.entries.where((e) => e.value > 0).map((e) {
      final pct = total > 0 ? ((e.value as num) / total * 100).toStringAsFixed(1) : '0';
      return ListTile(
        dense: true,
        title: Text(e.key.toString().replaceAll('_', ' '), style: const TextStyle(textBaseline: TextBaseline.alphabetic)),
        trailing: Text('${e.value} ($pct%)', style: const TextStyle(fontWeight: FontWeight.bold)),
      );
    }).toList();
  }

  int _totalDetections() {
    final counts = _stats['detection_counts'] as Map? ?? {};
    return counts.values.fold<int>(0, (a, b) => a + (b as int? ?? 0));
  }

  String _successRate() {
    final emitted = _events['total_emitted'] ?? 0;
    final handled = _events['total_handled'] ?? 0;
    if (emitted == 0) return '0';
    return ((handled / emitted) * 100).toStringAsFixed(1);
  }

  String _formatUptime(dynamic seconds) {
    final s = (seconds is num) ? seconds.toInt() : 0;
    final h = s ~/ 3600;
    final m = (s % 3600) ~/ 60;
    if (h > 0) return '${h}h ${m}m';
    return '${m}m';
  }
}
